USE NINEAM_CLASS;
SELECT ORDER_DATE , DATE_FORMAT(ORDER_DATE , "%a") AS SHORT_DAYNAME,
 DATE_FORMAT(ORDER_DATE , "%b") AS SHORT_MONTYNAME,
DATE_FORMAT(ORDER_DATE , "%c") AS SHORT_NUMERIC_MONTH,
 DATE_FORMAT(ORDER_DATE , "%D") AS SHORT_SUFFIX_MONTYNAME,
  DATE_FORMAT(ORDER_DATE , "%d") AS SHORT_SUFFIX_MONTYNAME,
   DATE_FORMAT(ORDER_DATE , "%e") AS SHORT_NUMERIC_MONTYNAME,
    DATE_FORMAT(ORDER_DATE , "%f") AS MICRO_SECONDS,
     DATE_FORMAT(ORDER_DATE , "%H") AS HOUR,
     DATE_FORMAT(ORDER_DATE , "%h") AS HOUR,
       DATE_FORMAT(ORDER_DATE , "%I") AS HOUR,
         DATE_FORMAT(ORDER_DATE , "%i") AS MINUTES,
           DATE_FORMAT(ORDER_DATE , "%j") AS DAYOF_THEYEAAR,
DATE_FORMAT(ORDER_DATE , "%k") AS HOUR,
  DATE_FORMAT(ORDER_DATE , "%l") AS HOUR,
    DATE_FORMAT(ORDER_DATE , "%M") AS NAME_IN_FULL,
      DATE_FORMAT(ORDER_DATE , "%m") AS NUMERIC_MONTH,
        DATE_FORMAT(ORDER_DATE , "%p") AS AM_PM,
  DATE_FORMAT(ORDER_DATE , "%r") AS TIME_FORMAT,
    DATE_FORMAT(ORDER_DATE , "%S") AS SECONDS,
      DATE_FORMAT(ORDER_DATE , "%s") AS SECONDS,
        DATE_FORMAT(ORDER_DATE , "%T") AS TIME_HOUR_FORMAT,
  DATE_FORMAT(ORDER_DATE , "%U") AS SUNDAY_WEEK,
  DATE_FORMAT(ORDER_DATE , "%u") AS SUNDAY_WEEK_SMALL,
  DATE_FORMAT(ORDER_DATE , "%V") AS SUNDAY_WEEK_FIRST,
  DATE_FORMAT(ORDER_DATE , "%v") AS SUNDAY_WEEK_SMALL,
  DATE_FORMAT(ORDER_DATE , "%W") AS WEEKDAY_FULLNAME,
  DATE_FORMAT(ORDER_DATE , "%w") AS DAY_WEEK,
  DATE_FORMAT(ORDER_DATE , "%X") AS YEAR_WEEK,
  DATE_FORMAT(ORDER_DATE , "%x") AS SUNDAY_WEEK_SMALL,
  DATE_FORMAT(ORDER_DATE , "%Y") AS YEAR_NUMERIC,
  DATE_FORMAT(ORDER_DATE , "%y") AS SUNDAY_WEEK
  FROM DATE_DATA_ORDERS;